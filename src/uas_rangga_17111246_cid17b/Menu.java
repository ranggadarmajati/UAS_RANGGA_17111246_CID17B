    private void btnSaveKryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveKryActionPerformed
        if( txtName.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Nama Harus diisi!");
            }else if("".equals(txtNik.getText())){
                JOptionPane.showMessageDialog(null, "Nik Harus diisi!");
            }else if("".equals( txtUsername.getText())){
                JOptionPane.showMessageDialog(null, "Username Harus diisi!");
            }else if("".equals( txtUsername.getText())){
                JOptionPane.showMessageDialog(null, "Password Harus diisi!");
            }else if( jDateOfBirth.getDate() == null){
                JOptionPane.showMessageDialog(null, "Tanggal Lahir Harus diisi!");
            }else if("".equals(txtPhone.getText())){
                JOptionPane.showMessageDialog(null, "No Hp Harus diisi!");
            }else if("".equals(txtAddress.getText())){
                JOptionPane.showMessageDialog(null, "Alamat Harus diisi!");
            }else if("-".equals(cmbRole.getSelectedItem())){
                JOptionPane.showMessageDialog(null, "Role Harus diisi!");
            }else{
        try {
            java.util.Date TglLahir = jDateOfBirth.getDate();
            java.sql.Date sqldate = new java.sql.Date(TglLahir.getTime());
            User user = new User();
            user.nik = txtNik.getText();
            user.name = txtName.getText();
            user.username = txtUsername.getText();
            user.password = txtUsername.getText();
            user.birth_of_date = sqldate;
            user.phone = txtPhone.getText();
            user.address = txtAddress.getText();
            user.role = (String) cmbRole.getSelectedItem();
            user.transactionUsers("save");
            getDataKaryawan("get all");
            setNullValue();
            setDefaultBtnKaryawan();
        } catch (IOException ex) {
            Logger.getLogger(Menu.class.getName()).log(Level.SEVERE, null, ex);
        }
      }
    }//GEN-LAST:event_btnSaveKryActionPerformed

