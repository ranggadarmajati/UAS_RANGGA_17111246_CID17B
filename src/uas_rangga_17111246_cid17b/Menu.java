    private void getDataItem(String type){
        DefaultTableModel tb= new DefaultTableModel();
        tb.addColumn("ID");
        tb.addColumn("SUPPLIER");
        tb.addColumn("JENIS");
        tb.addColumn("NAMA");
        tb.addColumn("HARGA BELI");
        tb.addColumn("HARGA JUAL");
        tb.addColumn("STOK");
        tb.addColumn("BARCODE");
        tb.addColumn("PLU");
        tblItem.setModel(tb);
        try{
            if("search".equals(type)){
                sql = "SELECT i.id as id, s.id as sid, s.name as supplier, t.id as tid, t.name as jenis, i.name, i.price_buy, i.price_sale, i.stock, i.barcode, i.plu FROM item i JOIN supplier s on i.supplier_id = s.id JOIN type_item t ON i.type_item_id = t.id WHERE concat(i.name, i.barcode, i.plu) like '%"+txtSearchItem.getText()+"%'";
            }else{
                sql = "SELECT i.id as id, s.id as sid, s.name as supplier, t.id as tid, t.name as jenis, i.name, i.price_buy, i.price_sale, i.stock, i.barcode, i.plu FROM item i JOIN supplier s on i.supplier_id = s.id JOIN type_item t ON i.type_item_id = t.id";
            }
            
            pst =con.prepareStatement(sql);
            rs = pst.executeQuery();
            
            while(rs.next()){
                tb.addRow(new Object[]{
                    rs.getString("id"),
                    rs.getString("supplier"),
                    rs.getString("jenis"),
                    rs.getString("name"),
                    rs.getString("price_buy"),
                    rs.getString("price_sale"),
                    rs.getString("stock"),
                    rs.getString("barcode"),
                    rs.getString("plu")
                });
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
